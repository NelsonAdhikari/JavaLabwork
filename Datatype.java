package atm;

public class Datatype {
   public static void main(String[]args) {
	   boolean b = true;
	   System.out.println("value is correct:"+b);
	   
	   int num=10;
	   System.out.println("value of number:"+num);
	   
	   byte y= 123;
	   System.out.println("Value of byte:"+y);
	   
	   float fn= 123.43f;
	   System.out.println("Value of float:"+fn);
	   
	   char name='n';
	   System.out.println("Value of char:"+name);
	   
	   short qn=23;
	   System.out.println("Value of short:"+qn);
	   
	   long qw=123343546;
	   System.out.println("Value of long:"+qw);
	   
	   double qr=123.4545;
	   System.out.println("Value of double:"+qr);
   }
}


